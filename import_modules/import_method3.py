# We import python module numpy to use integer methods
import numpy

# we call ceil and floor methods from numpy
print("numpy.ceil(1.2)    :", numpy.ceil(1.2))
print("numpy.ceil(1.933)  : ", numpy.ceil(1.933))

print("numpy.floor(1.2)   :", numpy.floor(1.2))
print("numpy.floor(1.933) :", numpy.floor(1.933))